package com.application.cricgod.basicapi.beans;

public class TeamRequestFlagsBean {
	
	/** Flag for Team Name */
	private boolean teamNameFlag;
	
	/** Flag for Squad Details */
	private boolean squadDetailsFlag;
	
	/** Flag for Fixture Details */
	private boolean fixtureDetailsFlag;
	
	/** Flag for Social-Media Details */
	private boolean socialMediaDetailsFlag;
	
	/** Flag for Home-Ground Details */
	private boolean homeGroundDetailsFlag;
	
	/** Flag for Owner Details */
	private boolean ownerDetailsFlag;

	/**
	 * @return the teamNameFlag
	 */
	public boolean isTeamNameFlag() {
		return teamNameFlag;
	}

	/**
	 * @param teamNameFlag the teamNameFlag to set
	 */
	public void setTeamNameFlag(boolean teamNameFlag) {
		this.teamNameFlag = teamNameFlag;
	}

	/**
	 * @return the squadDetailsFlag
	 */
	public boolean isSquadDetailsFlag() {
		return squadDetailsFlag;
	}

	/**
	 * @param squadDetailsFlag the squadDetailsFlag to set
	 */
	public void setSquadDetailsFlag(boolean squadDetailsFlag) {
		this.squadDetailsFlag = squadDetailsFlag;
	}

	/**
	 * @return the fixtureDetailsFlag
	 */
	public boolean isFixtureDetailsFlag() {
		return fixtureDetailsFlag;
	}

	/**
	 * @param fixtureDetailsFlag the fixtureDetailsFlag to set
	 */
	public void setFixtureDetailsFlag(boolean fixtureDetailsFlag) {
		this.fixtureDetailsFlag = fixtureDetailsFlag;
	}

	/**
	 * @return the socialMediaDetailsFlag
	 */
	public boolean isSocialMediaDetailsFlag() {
		return socialMediaDetailsFlag;
	}

	/**
	 * @param socialMediaDetailsFlag the socialMediaDetailsFlag to set
	 */
	public void setSocialMediaDetailsFlag(boolean socialMediaDetailsFlag) {
		this.socialMediaDetailsFlag = socialMediaDetailsFlag;
	}

	/**
	 * @return the homeGroundDetailsFlag
	 */
	public boolean isHomeGroundDetailsFlag() {
		return homeGroundDetailsFlag;
	}

	/**
	 * @param homeGroundDetailsFlag the homeGroundDetailsFlag to set
	 */
	public void setHomeGroundDetailsFlag(boolean homeGroundDetailsFlag) {
		this.homeGroundDetailsFlag = homeGroundDetailsFlag;
	}

	/**
	 * @return the ownerDetailsFlag
	 */
	public boolean isOwnerDetailsFlag() {
		return ownerDetailsFlag;
	}

	/**
	 * @param ownerDetailsFlag the ownerDetailsFlag to set
	 */
	public void setOwnerDetailsFlag(boolean ownerDetailsFlag) {
		this.ownerDetailsFlag = ownerDetailsFlag;
	}
	
	
}
